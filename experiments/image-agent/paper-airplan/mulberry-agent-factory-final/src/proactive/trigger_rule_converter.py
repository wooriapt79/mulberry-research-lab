    def _extract_time_hint(self, condition: str) -> str:
        """
        YAML condition 문자열에서 시간 범위를 파싱해 기본 로직 코드 생성.
        예: 'weekday 17:00-20:00 rain' → 17~20시 + 비 조건 코드
        """
        time_match = re.search(r"(\d{1,2}):(\d{2})-(\d{1,2}):(\d{2})", condition)
        weather_match = re.search(r"\b(rain|snow|hot|cold|clear)\b", condition, re.IGNORECASE)

        lines = []
        if time_match:
            sh, sm, eh, em = time_match.groups()
            # FIX(버그A): int() 변환 — "08"/"09" 문자열을 그대로 쓰면 Python 3 SyntaxError
            sh_int, eh_int = int(sh), int(eh)
            lines.append(f"if current_time and {sh_int} <= current_time.hour < {eh_int}:")
            if weather_match:
                lines.append(f'    if "{weather_match.group(1).lower()}" in weather:')
                lines.append( "        met = True")
                lines.append( "        confidence = 0.85")
            else:
                lines.append( "    met = True")
                lines.append( "    confidence = 0.80")
        elif weather_match:
            lines.append(f'if "{weather_match.group(1).lower()}" in weather:')
            lines.append( "    met = True")
            lines.append( "    confidence = 0.75")
        else:
            lines.append("# TODO: condition 파싱 불가 — 직접 구현 필요")
            lines.append(f"# condition: {condition}")

        return "\n        ".join(lines)

