#!/usr/bin/env python3
# src/proactive/trigger_rule_converter.py
"""
Mulberry Proactive Trigger Rule Converter
YAML 규칙 파일을 Python 클래스 코드로 자동 변환
- Spirit Score 검증 훅 자동 삽입
- 에지 퍼스트 실행 패턴 지원
- Korean Persona 톤 선택 연동
"""

import yaml
import json
import re
from pathlib import Path
from typing import Dict, List, Optional
from datetime import datetime

# YAML 입력 스키마 정의 (제언1: 변환 전 구조 검증용)
REQUIRED_RULE_KEYS = {"action", "condition"}
ALLOWED_CATEGORIES = {"time_based", "pattern_based", "context_based", "emotion_based"}


class TriggerRuleConverter:
    """
    YAML 형식의 프로액티브 트리거 규칙을
    실행 가능한 Python 클래스 코드로 변환
    """

    def __init__(self, output_dir: str = "src/proactive/rules"):
        self.output_dir = Path(output_dir)
        self.output_dir.mkdir(parents=True, exist_ok=True)

    def convert(self, yaml_path: str, class_name: Optional[str] = None) -> str:
        """
        YAML 파일 로드 → Python 클래스 코드 생성

        Args:
            yaml_path: 입력 YAML 파일 경로
            class_name: 생성할 클래스 이름 (생략 시 파일명 기반)

        Returns:
            생성된 Python 코드 문자열
        """
        # 1. YAML 로드
        # FIX(버그1): [yaml.safe](http://yaml.safe)_load → yaml.safe_load
        with open(yaml_path, "r", encoding="utf-8") as f:
            rules = yaml.safe_load(f)

        # 2. 제언1: YAML 스키마 검증 (변환 전 구조 오류 조기 감지)
        self._validate_yaml_schema(rules, yaml_path)

        # 3. 클래스 이름 결정
        if not class_name:
            class_name = Path(yaml_path).stem.replace("-", "_").title() + "Rules"

        # 4. 코드 생성
        code = self._generate_class_code(class_name, rules)

        # 5. 파일 저장
        output_path = self.output_dir / f"{class_name.lower()}.py"
        with open(output_path, "w", encoding="utf-8") as f:
            f.write(code)
        print(f"클래스 생성 완료: {output_path}")

        # 6. 제언3: 테스트 파일 자동 생성
        self._generate_test_file(class_name, rules)

        return code

    # ─────────────────────────────────────────
    # 제언1: YAML 스키마 검증
    # ─────────────────────────────────────────

    def _validate_yaml_schema(self, rules: Dict, source_path: str) -> None:
        """YAML 최상위 구조 및 각 규칙 필수 키 검증"""
        if not isinstance(rules, dict):
            raise ValueError(f"YAML 최상위가 dict가 아님: {source_path}")

        proactive = rules.get("proactive_rules")
        if not proactive:
            raise ValueError("YAML에 'proactive_rules' 키가 없음")

        unknown = set(proactive.keys()) - ALLOWED_CATEGORIES
        if unknown:
            raise ValueError(f"알 수 없는 카테고리: {unknown}")

        for category, rule_list in proactive.items():
            if not isinstance(rule_list, list):
                raise ValueError(f"'{category}'는 리스트여야 함")
            for i, rule in enumerate(rule_list):
                missing = REQUIRED_RULE_KEYS - set(rule.keys())
                if missing:
                    raise ValueError(
                        f"{category}[{i}] 필수 키 누락: {missing} — rule={rule}"
                    )

    # ─────────────────────────────────────────
    # 코드 생성
    # ─────────────────────────────────────────

    def _generate_class_code(self, class_name: str, rules: Dict) -> str:
        """클래스 코드 템플릿 기반 생성"""
        # FIX(버그1): [datetime.now](http://datetime.now)() → datetime.now()
        generated_at = datetime.now().isoformat()

        header = f'''#!/usr/bin/env python3
# Auto-generated by TriggerRuleConverter
# Generated at: {generated_at}
# Source: Mulberry Proactive Dual-Persona Architecture

from dataclasses import dataclass, field
from typing import Dict, List, Optional
from datetime import datetime
import logging

# FIX(버그4): src.ethics.ethics_gate → src.persona.ethics_gate
from src.persona.ethics_gate import EthicsGate

logger = logging.getLogger("{class_name}")

# ─────────────────────────────────────────
# {class_name}
# Proactive trigger rules converted from YAML
# Spirit Score validation hooks included
# ─────────────────────────────────────────

@dataclass
class TriggerCondition:
    """트리거 조건 평가 결과"""
    met: bool
    confidence: float              # 0.0 ~ 1.0
    context_summary: Dict          # 익명화 요약
    spirit_impact_estimate: float  # 0.0 ~ 1.0


@dataclass
class TriggerSignal:
    """활성화된 트리거 신호"""
    type: str
    confidence: float
    spirit_score: float
    summary: Dict


class {class_name}:
    """
    Auto-generated proactive trigger rules.
    All conditions evaluated locally (edge-first).
    Spirit Score validation required before activation.
    """

    def __init__(self, spirit_threshold: float = 0.75):
        # FIX(버그2): EthicsGate(threshold=x) → EthicsGate() + 별도 저장
        self.spirit_gate = EthicsGate()
        self.spirit_threshold = spirit_threshold

'''

        code = header
        proactive = rules.get("proactive_rules", {})

        if "time_based" in proactive:
            code += self._generate_time_based_methods(proactive["time_based"])
        if "pattern_based" in proactive:
            code += self._generate_pattern_based_methods(proactive["pattern_based"])
        if "context_based" in proactive:
            code += self._generate_context_based_methods(proactive["context_based"])
        if "emotion_based" in proactive:
            code += self._generate_emotion_based_methods(proactive["emotion_based"])

        code += self._generate_evaluate_all_method(proactive)
        code += self._generate_utility_methods()

        return code

    # ─────────────────────────────────────────
    # 카테고리별 메서드 생성
    # ─────────────────────────────────────────

    def _generate_time_based_methods(self, rules: List[Dict]) -> str:
        methods = "\n    # ── Time-based triggers ──\n"
        for i, rule in enumerate(rules):
            # FIX(버그7): 인덱스 접미사로 메서드명 충돌 방지
            method_name = f"check_{rule['action'].replace('-', '_')}_{i}"
            condition = rule["condition"]
            spirit_check = rule.get("spirit_check", True)

            # 제언2: condition 문자열에서 시간대 힌트 추출 시도
            time_hint = self._extract_time_hint(condition)

            methods += f'''
    def {method_name}(self, context: Dict) -> TriggerCondition:
        """
        Condition: {condition}
        Action: {rule['action']}
        Spirit Check: {spirit_check}
        """
        current_time = context.get("current_time")  # datetime object
        weather = context.get("weather", "").lower()

        met = False
        confidence = 0.0

        # 제언2: YAML condition 기반 자동 생성된 기본 로직
        # condition="{condition}"
        {time_hint}

        context_summary = {{
            "time_window": str(current_time.hour) if met and current_time else None,
            "weather_pattern": weather if met else None,
            "anonymized": True,
        }}

        return TriggerCondition(
            met=met,
            confidence=confidence,
            context_summary=context_summary,
            spirit_impact_estimate=0.90 if met else 0.0,
        )
'''
        return methods

    def _generate_pattern_based_methods(self, rules: List[Dict]) -> str:
        methods = "\n    # ── Pattern-based triggers ──\n"
        for i, rule in enumerate(rules):
            # FIX(버그7): 인덱스 접미사
            method_name = f"check_{rule['action'].replace('-', '_')}_{i}"
            condition = rule["condition"]
